class Paciente():

	def __init__(self, nome, ident, imagem):
		self.__nome = nome
		self.__ident = ident
		self.__imagem = imagem

	def setNome (self):
		self.__nome = nome

	def getNome(self):
		return self.__nome

	def setIDent(self):
		self.__ident = ident

	def getIDent(self):
		return self.__ident

	def setImg(self):
		self.__imagem = imagem

	def getImg(self):
		return self.__imagem